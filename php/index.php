
<p ><b><span class="titre">Introduction </span></b></p>

<p> <a name="introduction"></a>Le langage Java  a subi plusieurs changements depuis JDK 1.0, ainsi que de nombreuses additions de classes et de packages à la bibliothèque standard.</p>
<p> Java 8 est sorti en Mars 2014. Les modifications apportées à Java étaient plus profondes que toute modificationdans son histoire. Ces changements ajoutent le concept de la programmation fonctionnelle qui permet d’écrire des programmes plus facilement. </p>


<p class=MsoListParagraphCxSpMiddle><b><span style='font-size:26.0pt;
line-height:115%'>&nbsp;</span></b></p>


<p> <a name="syntaxe"></a><b><span style='font-size:18.0pt;line-height:115%;font-family:"Georgia","serif";color:#244061'>C’est quoi la programmation fonctionnelle ?</span></b></p>

<p class=MsoListParagraphCxSpMiddle><b><span style='font-size:26.0pt;
line-height:115%'>&nbsp;</span></b></p>
<p>La programmation fonctionnelle est un paradigme de programmation qui considère le calcul en tant qu'évaluation de fonctions.
La programmation fonctionnelle repose sur plusieurs principes :
</p>
<p class=MsoListParagraphCxSpMiddle><b><span style='font-size:26.0pt;
line-height:115%'>&nbsp;</span></b></p>

<p> <b>Immutabilité :</b></p>

<p>Ce concept permet de s'assurer de la valeur des variables du début à la fin de nos actions. Cela va permettre d'avoir un code plus robuste et plus stable, donc moins de bugs et moins de maintenances. Une fonction ne doit pas modifier les variables qui lui sont passées en paramètre.</p>
<p class=MsoListParagraphCxSpMiddle><b><span style='font-size:26.0pt;
line-height:115%'>&nbsp;</span></b></p>

<p> <b>Les fonctions d’ordre supérieur :</b></p>

<p>Un mécanisme puissant des langages fonctionnels est l'usage des fonctions d'ordre supérieur. Une fonction est dite d'ordre supérieur lorsqu'elle peut prendre des fonctions comme arguments (aussi appelées callback) et/ou renvoyer une fonction comme résultat. On dit aussi que les fonctions sont des objets de première classe, ce qui signifie qu'elles sont manipulables aussi simplement que les types de base.</p>
<p class=MsoListParagraphCxSpMiddle><b><span style='font-size:26.0pt;
line-height:115%'>&nbsp;</span></b></p>


<p> <b>Récursivité :</b></p>

<p>Il est possible d'utiliser la programmation fonctionnelle de manière récursive.Cela va permettre d'avoir un code plus lisible et plus court. De plus, le code s'auto documenter de lui-même.</p>
<p class=MsoListParagraphCxSpMiddle><b><span style='font-size:26.0pt;
line-height:115%'>&nbsp;</span></b></p>


<p><b><span style='font-size:18.0pt;line-height:115%;font-family:"Georgia","serif";color:#244061'>Java 8 comme un langage fonctionnel</span></b></p>
<p class=MsoListParagraphCxSpMiddle><b><span style='font-size:26.0pt;
line-height:115%'>&nbsp;</span></b></p>
<p>Java 8 introduit un nouveau API (Streams) qui supporte plusieurs opérations de processus des données et ressemble les requêtes des langages de base de données.</p>


<p>L’addition des Streams au Java peut être considérée comme une cause directe des deux autres additions : les techniques concises pour passer le code aux méthodes (les expressionsLambda et les références des méthodes), et les méthodes par défaut dans les interfaces.</p>

<p>Supposant qu’on veut écrire deux méthodes qui diffèrent en seulement quelque lignes de code ; on peut maintenant passer juste le code des parties qui diffèrent comme argument, cette technique de programmation est  plus courte, plus claire et plus fiable que la tendance commune à copier et coller tout le code.</p>


<p>On passant du code aux méthodes (et aussi être capable de les retourner et l’intégrer dans des structures de données),on fournit un accès à un ensemble de techniques supplémentaires qui sont connus comme un style de programmation fonctionnelle. Par conséquent, Java 8 fait un pas vers la programmation fonctionnelle.</p>

